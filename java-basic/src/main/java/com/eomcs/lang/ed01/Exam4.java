package com.eomcs.lang.ed01;

public class Exam4{
  public static void main(String[]args) {
    System.out.println(0x64);//2진수를 16진수로 바꿔서 표기한것
    System.out.println(0B00111001);//57라는10진수를 2진수로바꿔보겠다.
    System.out.println(0x39);//위에 2진수를 16진수 표기법으로 바꿔보았다.
    System.out.println(0xfe);//254값을 구해보자
    System.out.println(0B101101011);//값은363이나온다 16진수는
    System.out.println(0x16B);
  }
}